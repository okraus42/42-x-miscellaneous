#include <stdio.h>

int	main(void)
{
	char	a = 'A';
	char	b = 'b';
	char	c = ' ';
	char	d = '_';

	printf("<%c> | <%c> = <%c>\n", a, c, a | c);
	printf("<%c> & <%c>= <%c>\n", b, d, b & d);
	return (0);
}